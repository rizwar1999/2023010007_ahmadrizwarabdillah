let temperature = 30;

if (temperature >= 30) {
    console.log("Di luar panas!");
} else if (temperature >= 20) {
    console.log("Cuacanya hangat.");
} else if (temperature >= 10) {
    console.log("Agak dingin.");
} else if (temperature >= 0) {
    console.log("Di luar dingin.");
} else {
    console.log("Dingin sekali!");
}

// output : "Di luar panas!"