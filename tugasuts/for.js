const buah = ["apel", "pisang", "jeruk", "mangga"];

for (let i = 0; i < buah.length; i++) {
    console.log("Buah ke-" + (i + 1) + ": " + buah[i]);
}

console.log("Selesai iterasi buah!");


/* output : Buah ke-1: apel
            Buah ke-2: pisang
            Buah ke-3: jeruk
            Buah ke-4: mangga
            Selesai iterasi buah!*/
