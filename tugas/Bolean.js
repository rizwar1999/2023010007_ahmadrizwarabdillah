// Mendefinisikan variabel boolean
let isKurmaAvailable = true;
let isOutOfStock = false;

// Contoh penggunaan
if (isKurmaAvailable) {
    console.log("Kurma tersedia untuk dibeli.");
} else {
    console.log("Maaf, kurma tidak tersedia saat ini.");
}

if (isOutOfStock) {
    console.log("Maaf, stok kurma telah habis.");
} else {
    console.log("Stok kurma masih tersedia.");
}
